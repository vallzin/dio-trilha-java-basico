public class OperadoresRelacionais {
    public static void main(String[] args) {
        int numeroUm = 1;
        int numeroDois = 2;

        boolean simNao = numeroUm == numeroDois;

        System.out.println("O numeroUm é igual a numeroDois? "+simNao);

        simNao = numeroUm != numeroDois;

        System.out.println("O numeroUm é diferente de numeroDois? "+simNao);

        
    }
}
